"""
Напишите генератор, который будет принимать на вход числа и возвращать их сумму.
Генератор должен использовать инструкцию yield для возврата текущей суммы и
должен продолжать принимать новые числа для добавления к сумме. Если генератор получает на вход число 0,
он должен прекращать работу и вернуть окончательную сумму.
Напишите программу, которая будет использовать этот генератор для пошагового расчета суммы чисел,
вводимых пользователем.


Пример вывода:


Введите числа для суммирования (0 для окончания):

Введите число: 3
Текущая сумма: 3
Введите число: 5
Текущая сумма: 8
Введите число: 2
Текущая сумма: 10
Введите число: 0
Текущая сумма: 10
"""


def sum_generator():
    total = 0
    while True:
        num = yield total
        if num == 0:
            return total
        total += num


def calculate_sum():
    print("Введите числа для суммирования (0 для окончания):")
    gen = sum_generator()
    next(gen)
    while True:
        try:
            num = int(input("Введите число: "))
            total = gen.send(num)
            print(f"Текущая сумма: {total}")
        except StopIteration as e:
            print(f"Текущая сумма: {e.value}")
            break


calculate_sum()
